// File header contenente solo i prototipi con relativa documentazione

/**
 * Ritorna il numero di cifre che compongono un mumero
 * 
 * @param int Valore da analizzare.
 * @return Numero delle cifre.
 */
int numberOfDigits(int _n);

/**
 * Ritorna il valore medio tra le cifre che compongono un numero.
 * 
 * @param int Valore da analizzare.
 * @return media delle cifre.
 */
int avarageValueOfDigits(int _n);

/**
 * Restituisce la cifra maggiore che compare nel numero passato come parametro.
 * 
 * @param int Valore da analizzare.
 * @return Cifra maggiore che compare.
 */
int majorDigit(int _n);

/**
 * Riceve un valore e lo restituisce capovolto
 * 
 * @param int Valore da analizzare.
 * @return Valore ricomposto al contrario.
 */
int reversedNumber(int _n);

//--- FUNZIONI PER ESERCIZIO 2 ---

/**
 * Restituisce la somma delle cifre di un numero passato come parametro.
 * 
 * @param int Valore da analizzare.
 * @return Somma delle cifre del valore passato.
 */
int sommaCifre(int _n);

/**
 * Restituisce un numero random la cui somma delle cifre è pari alla somma delle cifre del numero passato
 * 
 * @param int Valore da analizzare.
 * @return Nuovo numero determinato.
 */
int numeroEquivalente(int _n);